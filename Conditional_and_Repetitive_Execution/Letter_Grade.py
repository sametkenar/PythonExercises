total_point = int(input())

if 0 <= total_point < 50:
    print("FF")
elif 50 <= total_point < 60:
    print("FD")
elif 60 <= total_point < 65:
    print("DD")
elif 65 <= total_point < 70:
    print("DC")
elif 70 <= total_point < 75:
    print("CC")
elif 75 <= total_point < 80:
    print("CB")
elif 80 <= total_point < 85:
    print("BB")
elif 85 <= total_point < 90:
    print("BA")
elif 90 <= total_point <= 100:
    print("AA")